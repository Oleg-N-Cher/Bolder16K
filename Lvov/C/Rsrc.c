unsigned char Rsrc_field [256];

/*--------------------------------- Cut here ---------------------------------*/
extern const unsigned char _Rsrc_Tiles [Rsrc_TileSize * 18 + Rscr_TilePad];

Rsrc_Resource Rsrc_GetTileByNum (unsigned char num) {
  return &_Rsrc_Tiles + num*48;
/*
    LD   H, #0
    LD   C, L   ; L = num
    LD   B, H
    ADD  HL, HL
    ADD  HL, BC
    ADD  HL, HL
    ADD  HL, HL
    ADD  HL, HL ; tile*24
    LD   BC, #__Rsrc_Tiles
    ADD  HL, BC ; + _Rsrc_Tiles
*/
} //Rsrc_GetTileByNum

/*--------------------------------- Cut here ---------------------------------*/
unsigned char Rsrc_GetCell (unsigned char x, unsigned char y) {
// RETURN field [y DIV 2 * FieldWidth + x DIV 2]
  return Rsrc_field[__ASHL(__ASHR(y, 1, SHORTINT), 4, SHORTINT) + __ASHR(x, 1, SHORTINT)];
/*
    POP  HL
    EX   (SP), HL
    LD   A, H   ; y
    SRL  A      ; y DIV 2
    ADD  A
    ADD  A
    ADD  A
    ADD  A      ; y DIV 2 * FieldWidth
    SRL  L      ; x DIV 2
    ADD  L      ; y DIV 2 * FieldWidth + x DIV 2
    LD   L, A
    LD   H, #>_Rsrc_field
    LD   L, (HL)
    RET
*/
} //Rsrc_GetCell

/*--------------------------------- Cut here ---------------------------------*/
unsigned char Rsrc_SetCell (unsigned char x, unsigned char y, unsigned char cell) {
// field [y DIV 2 * FieldWidth + x DIV 2] := cell
  Rsrc_field[__ASHL(__ASHR(y, 1, SHORTINT), 4, SHORTINT) + __ASHR(x, 1, SHORTINT)] = cell;
/*
    POP  DE
    POP  HL     ; L = x; H = y
    DEC  SP
    POP  BC     ; B = cell
    PUSH DE
    LD   A, H   ; y
    SRL  A      ; y DIV 2
    ADD  A
    ADD  A
    ADD  A
    ADD  A      ; y DIV 2 * FieldWidth
    SRL  L      ; x DIV 2
    ADD  L      ; y DIV 2 * FieldWidth + x DIV 2
    LD   L, A
    LD   H, #>_Rsrc_field
    LD   (HL), B
    RET
*/
} //Rsrc_SetCell

